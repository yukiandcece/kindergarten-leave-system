/**
 * 获取图标HTML
 * @param {string} iconName - 图标名称
 * @param {string} className - 自定义类名
 * @returns {string} SVG HTML字符串
 */
export function getIcon(iconName, className = 'icon') {
  const icon = Icons[iconName];
  if (!icon) {
    console.warn(`Icon "${iconName}" not found`);
    return '';
  }
  return `<span class="${className}">${icon}</span>`;
}

/**
 * 创建图标元素
 * @param {string} iconName - 图标名称
 * @param {string} className - 自定义类名
 * @returns {HTMLElement} 图标元素
 */
export function createIconElement(iconName, className = 'icon') {
  const wrapper = document.createElement('span');
  wrapper.className = className;
  wrapper.innerHTML = Icons[iconName] || '';
  return wrapper;
}